package Paypal;

public class Details {
	
	private String subtotal;
	
	private String tax;
	
	private String shipping;
	
	private String handling_fee;
	
	private String shipping_discount;
	
	private String insurance;

	public final String getSubtotal() {
		return subtotal;
	}

	public final void setSubtotal(String subtotal) {
		this.subtotal = subtotal;
	}

	public final String getTax() {
		return tax;
	}

	public final void setTax(String tax) {
		this.tax = tax;
	}

	public final String getShipping() {
		return shipping;
	}

	public final void setShipping(String shipping) {
		this.shipping = shipping;
	}

	public final String getHandling_fee() {
		return handling_fee;
	}

	public final void setHandling_fee(String handling_fee) {
		this.handling_fee = handling_fee;
	}

	public final String getShipping_discount() {
		return shipping_discount;
	}

	public final void setShipping_discount(String shipping_discount) {
		this.shipping_discount = shipping_discount;
	}

	public final String getInsurance() {
		return insurance;
	}

	public final void setInsurance(String insurance) {
		this.insurance = insurance;
	}

	

}
